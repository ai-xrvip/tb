def get_current_paywall(role_id: str, message_count: int, current_tier: int) -> dict | None:
    """根据消息数和当前层级，返回下一个付费门槛（如果有）"""
    paywalls = get_paywall(role_id)
    for pw in paywalls:
        if pw["tier"] > current_tier and message_count >= pw["message_threshold"]:
            return pw
    return None
